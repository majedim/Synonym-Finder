public class contact {
    int id;
    String word, synonym;

    public void setId(int id)
    {
        this.id = id;
    }
    public int getId()
    {
        return this.id;
    }


    public void setWord(String word)
    {
        this.word = word;
    }
    public String getWord()
    {
        return this.word;
    }

    public void setSynonym(String synonym)
    {
        this.synonym = synonym;
    }
    public String getSynonym()
    {
        return this.synonym;
    }


}

